/**
 * @file DnD MIME type constants for ribbon menu drag operations.
 */

/** Palette item being dragged into a group. */
export const PALETTE_MIME = "application/x-ribbon-palette-item";

/** Existing item being moved between/within groups. */
export const ITEM_MOVE_MIME = "application/x-ribbon-item-move";

/** Tab being reordered. */
export const TAB_REORDER_MIME = "application/x-ribbon-tab-reorder";

/** Group being reordered. */
export const GROUP_REORDER_MIME = "application/x-ribbon-grp-reorder";
